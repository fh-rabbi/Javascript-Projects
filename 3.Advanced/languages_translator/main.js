// Add all language in select menu
selectTag.forEach((tag,index)=>{
    for(let i in countries){
      let selected = index == 0 ? i == "en-GB" ? "selected" : "" : i == "hi-IN" ? "selected" : "";
      let option = `<option ${selected} value="${i}">${countries[i]}</option>`;
      tag.insertAdjacentHTML('beforeend',option);
    }
});


// Main logic to translate text
function translate(){
  let text = fromText.value;
  fromLang = selectTag[0].value;
  toLang = selectTag[1].value;
  if(!text){
    toText.value = '';
    toText.setAttribute('placeholder','Translation');
    return;
  }
  console.log('fire');
  toText.setAttribute('placeholder','Translating...');
  let apiUrl = `https://api.mymemory.translated.net/get?q=${text}&langpair=${fromLang}|${toLang}`;
  fetch(apiUrl)
  .then(res=>res.json())
  .then(data=>{
    console.log(data);
    toText.value = data.responseData.translatedText;
    toText.setAttribute('placeholder','Translation');
  })
  .catch(err=>console.log(err));
}
button.addEventListener("click", translate);


// Exchange language when clicked on Exchange Button
exchangeBtn.addEventListener("click", ()=>{
  let tempText = fromText.value,
    tempLang = selectTag[0].value;
    fromText.value = toText.value;
    toText.value = tempText;
    selectTag[0].value = selectTag[1].value;
    selectTag[1].value = tempLang;
});
